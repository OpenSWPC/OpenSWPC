  !! color palette
  !!
  !! v1 (R G B)  -  v2 (R G B)
  !! colors between v1 and v2 are interpolated linearly
  !<
  !! --
  type color__palette

    integer               :: nc
    real(DP), allocatable :: v1(:), v2(:)
    integer,  allocatable :: col1(:,:), col2(:,:)
    integer               :: col_b(3), col_f(3), col_n(3)
    logical               :: initialized = .false.

  end type color__palette
